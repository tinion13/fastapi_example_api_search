def wind_direction_16(degrees):
    degrees = degrees % 360

    directions = [
        (348.75, 11.25, "С"),      (11.25, 33.75, "ССВ"),
        (33.75, 56.25, "СВ"),      (56.25, 78.75, "ВСВ"),
        (78.75, 101.25, "В"),      (101.25, 123.75, "ВЮВ"),
        (123.75, 146.25, "ЮВ"),    (146.25, 168.75, "ЮЮВ"),
        (168.75, 191.25, "Ю"),     (191.25, 213.75, "ЮЮЗ"),
        (213.75, 236.25, "ЮЗ"),    (236.25, 258.75, "ЗЮЗ"),
        (258.75, 281.25, "З"),     (281.25, 303.75, "ЗСЗ"),
        (303.75, 326.25, "СЗ"),    (326.25, 348.75, "ССЗ")
    ]

    for start, end, dir_name in directions:
        if start <= degrees < end:
            return dir_name

    return "С"
